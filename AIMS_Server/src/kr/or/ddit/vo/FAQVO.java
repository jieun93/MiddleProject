package kr.or.ddit.vo;

import java.io.Serializable;

public class FAQVO implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1236440654980549006L;
	private String faq_no;
	private String faq_title;
	private String faq_content;
	
	//게더세더
	public String getFaq_no() {
		return faq_no;
	}
	public void setFaq_no(String faq_no) {
		this.faq_no = faq_no;
	}
	public String getFaq_title() {
		return faq_title;
	}
	public void setFaq_title(String faq_title) {
		this.faq_title = faq_title;
	}
	public String getFaq_content() {
		return faq_content;
	}
	public void setFaq_content(String faq_content) {
		this.faq_content = faq_content;
	}
	
	//생성자
	public FAQVO() { }
	
	
	public FAQVO(String faq_no, String faq_title, String faq_content) {
		super();
		this.faq_no = faq_no;
		this.faq_title = faq_title;
		this.faq_content = faq_content;
	}
	
	
	

}
